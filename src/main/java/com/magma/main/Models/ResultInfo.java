package com.magma.main.Models;

/**
 * @author MhdHawa
 */
public class ResultInfo {

    private final int code;
    private final String message;

    public ResultInfo(int code, String message) {
        this.code = code;
        this.message = message;
    }


    public int getCode() {
        return code;
    }

    public String getMessage() {
        return message;
    }
}
